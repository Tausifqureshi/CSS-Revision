// ============================================ Transform-Functions ============================================== //
//  ChatGPT Se liya hua Syntax ---> 2D- Transform-Functions.
// 1. translate(x, y): Element ko current position se move karta hai. x horizontal offset hai, aur y vertical offset hai.

// 2. rotate(angle): Element ko specified angle ke around rotate karta hai. Angles degrees (e.g., 45deg) ya radians (e.g., 0.785rad) me specify kiye jaate hain.

// 3. scale(x, y): Element ko horizontal (x) aur vertical (y) directions me scale karta hai. Values 1 se zyada size ko increase karti hain, aur values 1 se kam size ko decrease karti hain.

// 4. skew(x-angle, y-angle): Element ko X aur Y axes ke along specified angles se skew karta hai.

// 5. matrix(a, b, c, d, e, f): 2D transformation ko matrix ke through define karta hai. Ye function precise control deta hai lekin complex hota hai.

// ========================================= 3-D Transform-Functions ============================================ //
//  ChatGPT Se liya hua Syntax ---> CSS 3D transforms aapko elements par three-dimensional space me transformations apply karne ki suvidha dete hain. Isme aap rotation, translation, aur scaling elements ko X, Y, aur Z axes ke along kar sakte hain, jo depth aur perspective effects create karte hain.

// Key 3D Transform Functions.
// 1. rotateX(angle): Element ko X-axis ke around specified angle se rotate karta hai.
// 2. rotateY(angle): Element ko Y-axis ke around specified angle se rotate karta hai.
// 3. rotateZ(angle): Element ko Z-axis ke around specified angle se rotate karta hai (2D rotation ke similar).
// 4. translateX(value): Element ko X-axis ke along move karta hai.
// 5. translateY(value): Element ko Y-axis ke along move karta hai.
// 6. translateZ(value): Element ko Z-axis ke along move karta hai, jo depth create karta hai.
// 7. scaleX(value): Element ko X-axis ke along scale karta hai.
// 8. scaleY(value): Element ko Y-axis ke along scale karta hai.
// 9. scaleZ(value): Element ko Z-axis ke along scale karta hai.
// 10. perspective(value): Viewer aur z=0 plane ke beech ki distance define karta hai, jo 3D elements ka perspective affect karta hai.

