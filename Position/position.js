
// ================================================= Position ==================================================== //
//  ChatGPT Se liya hua Syntax ---> Postion

// ============================================= Position Static ================================================ //
//  ChatGPT Se liya hua Syntax ---> 1. static
// Default Value: Jab aap position property set nahi karte, to element ka positioning default static hota hai.
// Behavior: Static elements document flow ke according place hote hain aur top, right, bottom, aur left properties ka effect nahi hota.


// ============================================ Position relative ================================================ //
//  ChatGPT Se liya hua Syntax --->  2. relative
// Behavior: Relative positioning element ko document flow ke respect me position karta hai, lekin top, right, bottom, aur left properties se position ko adjust kiya ja sakta hai.

// Effect: Element apne original position se adjust hota hai.


// ============================================ Position absolute ================================================ //
//  ChatGPT Se liya hua Syntax --->  3. absolute
// Behavior: Absolute positioning element ko nearest positioned ancestor ke respect me position karta hai (ancestor with position set to relative, absolute, or fixed). Agar koi positioned ancestor nahi hai, to element document body ke respect me position hota hai.

// Effect: Element document flow se completely remove ho jata hai aur top, right, bottom, aur left properties se position adjust hota hai.


// ============================================== Position Fixed ================================================= //
//  ChatGPT Se liya hua Syntax ---> 4. fixed
// Behavior: Fixed positioning element ko viewport ke respect me position karta hai, yani ki browser window ke respect me. Page scroll hone ke bawajood element fixed position par rahega.

// Effect: Element document flow se completely remove ho jata hai aur viewport ke respect me top, right, bottom, aur left properties se position adjust hota hai.


// ============================================= Position Sticky ================================================= //
//  ChatGPT Se liya hua Syntax ---> 5. sticky
// Behavior: Sticky positioning element ko scroll ke respect me position karta hai. Ye element tab tak apne normal flow position par rahega jab tak user scroll nahi karta aur us position par stick ho jata hai jab user scrolling ke karan us position ke pass aata hai.

// Effect: top, right, bottom, aur left properties se positioning adjust hoti hai jab element scroll position ke close aata hai.



