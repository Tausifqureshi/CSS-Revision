// =========================================== Transition-Property ============================================== //
//  ChatGPT Se liya hua Syntax --->  Transition-Property.

// 1. property: CSS property jo aap animate karna chahte hain (e.g., background-color, width, opacity).
// 2. duration: Transition kitni der tak chalega (e.g., 2s, 500ms).
// 3. timing-function: Transition ki speed curve (e.g., linear, ease, ease-in, ease-out).
// 4. delay: Transition shuru hone se pehle kitna intezar karna hai (e.g., 1s, 200ms).

// 5. Transition Timing Functions:
// Yeh functions transition ki speed ko control karte hain. Common timing functions hain:
// ease: Slow se start hota hai, middle me speed badhti hai, aur end me slow hota hai.
// linear: Constant speed se transition hoti hai.
// ease-in: Slow se start hota hai aur speed badhti hai.
// ease-out: Fast se start hota hai aur slow hota hai.
// ease-in-out: Slow se start aur end hota hai, aur middle me fast hota hai.
// cubic-bezier(x1, y1, x2, y2): Custom timing function jo Bezier curves use karta hai.


// 6. Duration aur Delay:
// duration ko seconds (s) ya milliseconds (ms) mein specify kiya ja sakta hai.
// delay specify karta hai ki transition shuru hone se pehle kitna intezar karna hai.