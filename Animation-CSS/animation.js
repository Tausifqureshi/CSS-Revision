// 1. @keyframes: This rule is used to define the animation. It specifies the styles that will be applied at different stages of the animation.

// 2. animation-name: Specifies the name of the @keyframes animation to be applied.

// 3. animation-duration: Defines how long the animation should take to complete one cycle.

// 4. animation-timing-function: Specifies the speed curve of the animation. Common values include ease, linear, ease-in, ease-out, and ease-in-out.

// 5. animation-delay: Specifies a delay before the animation starts.

// 6. animation-iteration-count: Defines how many times the animation should be played. It can be a number or infinite.

// 7. animation-direction: Specifies whether the animation should play in reverse on alternate cycles. Values can be normal, reverse, alternate, and alternate-reverse.

// 8. animation-fill-mode: Specifies the style applied to the element when the animation is not playing (before it starts, after it ends, or both).
