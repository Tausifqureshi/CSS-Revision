// ============================================ Key Pseudo-Elements ============================================== //
//  ChatGPT Se liya hua Syntax ---> 1. ::before:
// Yeh pseudo-element element ke content se pehle content insert karta hai. Yeh commonly additional decorative elements ya content add karne ke liye use hota hai.

// 2. ::after:
// Yeh pseudo-element element ke content ke baad content insert karta hai. Yeh bhi additional decorative elements ya content add karne ke liye use hota hai.

// 3. ::first-line:
// Yeh pseudo-element element ke pehle line ko style karta hai. Yeh typically paragraphs ya blocks of text me use hota hai.


// 4. ::first-letter:
// Yeh pseudo-element element ke pehle letter ko style karta hai. Yeh often decorative typography effects ke liye use hota hai.



