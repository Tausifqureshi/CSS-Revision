// 1. blur(): Element par Gaussian blur apply karta hai.

// 2. brightness(): Element ki brightness ko adjust karta hai. 100% se zyada values element ko bright banati hain, aur 100% se kam values use dark karti hain.

// 3. contrast(): Element ki contrast ko adjust karta hai. 100% se zyada values contrast ko increase karti hain, aur 100% se kam values decrease karti hain.

// 4. drop-shadow(): Element par shadow effect apply karta hai. Ye box-shadow ke similar hota hai lekin zyada flexible hota hai.

// 5. grayscale(): Element ko grayscale me convert karta hai. Values 0% (original color) se 100% (completely grayscale) tak hoti hain.

// 6. hue-rotate(): Element ke color ki hue ko rotate karta hai. Values degrees me specify hoti hain.

// 7. invert(): Element ke colors ko invert karta hai. Values 0% (no inversion) se 100% (full inversion) tak hoti hain.

// 8. opacity(): Element ki transparency ko adjust karta hai. Values 0% (fully transparent) se 100% (fully opaque) tak hoti hain.

// 9. saturate(): Element ki saturation ko adjust karta hai. 100% se zyada values saturation ko increase karti hain, aur 100% se kam values decrease karti hain.

// 10. sepia(): Element ko sepia filter apply karta hai, jo brownish tone deta hai. Values 0% (no sepia) se 100% (full sepia) tak hoti hain.







