// ============================================== Absolute Units ================================================= //
//  ChatGPT Se liya hua Syntax ---> 1. Absolute Units
// Absolute units fixed hote hain aur context ke anusar change nahi hote. Kuch common absolute units:

// 1. px (pixels): Sabse zyada use hone wala unit, screen par ek point ko darshata hai.
// 2. cm (centimeters): Physical measurement ko darshata hai. Screen layouts ke liye kam use hota hai.
// 3. mm (millimeters): cm ke jaisa, kam use hota hai.
// 4. in (inches): Ek inch 96 pixels ke barabar hota hai.
// 5. pt (points): 1 point 1/72 inch ke barabar hota hai.
// 6. pc (picas): 1 pica 12 points ke barabar hota hai.



// ============================================== Relative Units ================================================= //
//  ChatGPT Se liya hua Syntax ---> 2. Relative Units
// Relative units kisi doosre measurement ke anusar hote hain, jaise parent element ka size ya root element ka font size. Kuch common relative units:


// 1. em: Element ke font size ke anusar hota hai. Agar element ka font size 16px hai, to 1em 16px hoga.
// 2. rem: Root element (html) ke font size ke anusar hota hai. Ye site-wide consistent sizing banane ke liye useful hai.
// 3. % (percent): Parent element ke size ke anusar hota hai.
// 4. vw (viewport width): 1vw viewport ki width ka 1% ke barabar hota hai.
// 5. vh (viewport height): 1vh viewport ki height ka 1% ke barabar hota hai.
// 6. vmin: vw aur vh me se chhota unit hota hai.
// 7. vmax: vw aur vh me se bada unit hota hai.