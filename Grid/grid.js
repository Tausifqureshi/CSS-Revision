// 2. Grid Item: Grid container ke seedhe children hote hain. Yeh items aapke dwara defined grid structure mein place hote hain.

// 3. Grid Lines: Yeh lines grid ko cells mein divide karti hain. Yeh horizontal (rows) ya vertical (columns) ho sakti hain.

// 4. Grid Tracks: Do adjacent grid lines ke beech ka space. Ek track ek row ya column ho sakta hai.

// 5. Grid Cell: Grid ka sabse chhota unit, jo ek row aur ek column ke milan se banta hai.

